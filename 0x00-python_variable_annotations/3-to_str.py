#!/usr/bin/env python3

"""
This script defines a function named to_str that converts a floating-point
number to a string representation.
"""


def to_str(n: float) -> str:
    """
    This function converts a floating-point number to a string representation.

    Args:
        n (float): The floating-point number to be converted to a string.

    Returns:
        str: The string representation of the floating-point number.
    """

    return str(n)
