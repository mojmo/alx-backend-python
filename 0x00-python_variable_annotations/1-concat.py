#!/usr/bin/env python3

"""concatenates two strings"""


def concat(str1: str, str2: str) -> str:
    """
    This function concatenates two strings and returns the combined string.

    Args:
      str1: The first string.
      str2: The second string.

    Returns:
      The combined string of str1 and str2.
    """

    return str1 + str2
