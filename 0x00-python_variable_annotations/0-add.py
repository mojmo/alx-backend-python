#!/usr/bin/env python3
"""Adds two floats together and returns the sum."""


def add(a: float, b: float) -> float:
    """
    Adds two floats together and returns the sum.

    Args:
      a (float): The first float number.
      b (float): The second float number.

    Returns (float):
      The sum of a and b.
"""
    return a + b
